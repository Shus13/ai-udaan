# Graph based Model : Network - SSL
# Deep Q-Network : Neural Network - RL

# Deep Learning : DL is the modern branch of ML, jasley Artificial Neural Network use garxa for depth and detailed learning. It needs Big data and advanced GPU, ANN ma layers hunxa, input, hidden, output (just like in back propagation problem); eg: LLMs, Digital assistance