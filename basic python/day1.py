# Artificial Intelligence : Machine lai human le jastai think garna, learn garna, decision lina sakney banauney technology

# Machine Learning : ML is the branch of AI, jasly data liyeko aadhar ma aafai learn garney ani decision lina sakney banxa; eg: Email filtering, Recommendations,

# Types
# Supervised learning : machine lai labelled(input ko yo output hunxa) data diyera train garayinxa; 
# Un-Supervised learning : machine lai raw/unlabelled data ma train garainxa
# Semi-Supervised learning : It is the mixed up of both supervised and unsupervised; labelled and unlabelled data
# Reinforcement learning : machine ley error ani experience sangai learn gardai janxa, There is a rule called "Reward and Punishment"; Jaba better result lyauxa it get's reward points ani error badyo vaney reward points ghatxa eg: Chess playing machine, Self-driving cars

